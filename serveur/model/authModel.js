const mongoose = require("mongoose");

const schema = new mongoose.Schema({
  FirstName: {
    type: String,
    required: true,
  },
  LastName: {
    type: String,
    required: true,
  },
  Email: {
    type: String,
    required: true,
  },
  Password: {
    type: String,
    required: true,
  },
});

const Auth = mongoose.model("Auth", schema);

module.exports = Auth;
